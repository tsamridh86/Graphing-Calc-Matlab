function Plot_Callback(hObject, eventdata, handles)
% hObject    handle to Plot (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
a1 = str2double(get(handles.x1,'String'));
a2 = str2double(get(handles.x2,'String'));
b1 = str2double(get(handles.y1,'String'));
b2 = str2double(get(handles.y2,'String'));
str1 = get(handles.f1,'String');
str2 = get(handles.f2,'String');
func = vectorize(inline(str1,'x'));
funv = vectorize(inline(str2,'x'));
axes(handles.xyz1);
a = a1:0.01:a2;
plot(a,func(a),a,funv(a))
axis([a1 a2 b1 b2])
grid on;


